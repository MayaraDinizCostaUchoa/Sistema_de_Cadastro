    private void jTableUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuarioMouseClicked
        String nome_usuario = "" + jTableUsuario.getValueAt(jTableUsuario.getSelectedRow(), 1);
        connBanco.conexao();
        connBanco.executaSQL("select *from usuarios where nome_usuario='"+nome_usuario+"'");
        try {
            connBanco.rs.first();
            jTextFieldCodUsuario.setText(String.valueOf(connBanco.rs.getInt("cod_usuario")));
            jTextFieldUsuario.setText(connBanco.rs.getString("nome_usuario")); 
            jComboBoxTipo.setSelectedItem(connBanco.rs.getString("tipo_usuario")); 
            jPasswordFieldSenha.setText(connBanco.rs.getString("senha_usuario"));
            jPasswordFieldConfirmarSenha.setText(connBanco.rs.getString("senha_usuario"));
            connBanco.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar os dados!\nERRO:" + ex);
        }
        connBanco.desconecta();
        jButtonNovo.setEnabled(false);
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        jTextFieldCodUsuario.setEnabled(false);
        jTextFieldUsuario.setEnabled(false);
        jPasswordFieldSenha.setEnabled(false);
        jPasswordFieldConfirmarSenha.setEnabled(false);
        jComboBoxTipo.setEnabled(false);
    }//GEN-LAST:event_jTableUsuarioMouseClicked

