    public void preencherTabelaUsuario(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[] {"Cód.","Usuário","Tipo"};
        connBanco.conexao();
        connBanco.executaSQL(Sql);
        try{
            connBanco.rs.first();
            do{
                dados.add(new Object[]{connBanco.rs.getInt("cod_usuario"), connBanco.rs.getString("nome_usuario"), connBanco.rs.getString("tipo_usuario")});
                
            }while(connBanco.rs.next());
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayListUsu" + ex);
        }
        ModeloTabela modelo= new ModeloTabela(dados, colunas);
        
        jTableUsuario.setModel(modelo);
        jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTableUsuario.getColumnModel().getColumn(1).setResizable(false);        
        jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTableUsuario.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuario.getTableHeader().setReorderingAllowed(false);
        jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
        jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        connBanco.desconecta();
        
    }
    
