    private void jTableFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionariosMouseClicked
        String nome_funcionario = "" + jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 1);
        connBanco.conexao();
        connBanco.executaSQL("select * from funcionarios where nome_funcionario='"+nome_funcionario+"'");
        try {
            connBanco.rs.first();
            jTextFieldCodFuncionario.setText(String.valueOf(connBanco.rs.getInt("cod_funcionario")));
            jTextFieldNomeFuncionario.setText(connBanco.rs.getString("nome_funcionario")); 
            jFormattedTextFieldTelefoneFuncionario.setText(connBanco.rs.getString("telefone_funcionario"));
            jTextFieldRgFuncionario.setText(connBanco.rs.getString("rg_funcionario"));
            jFormattedTextFieldCpfFuncionario.setText(connBanco.rs.getString("cpf_funcionario"));
            jFormattedTextFieldData_de_Nascimento.setText(connBanco.rs.getString("data_de_nascimento"));        
            jTextFieldConselhoFuncionario.setText(connBanco.rs.getString("conselho"));
            jComboBoxProfissaoFuncionario.setSelectedItem(connBanco.rs.getString("profissao"));
            connBanco.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar os dados!\nERRO:" + ex);
        }
        connBanco.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        
    }//GEN-LAST:event_jTableFuncionariosMouseClicked

