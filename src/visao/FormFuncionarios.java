    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[] {"Cód.", "Nome", "Telefone", "Profissão", "Conselho", "CPF", "RG", "Data de Nascimento"};
        connBanco.conexao();
        connBanco.executaSQL(Sql);
        try{
            connBanco.rs.first();
            do{
                dados.add(new Object[]{connBanco.rs.getInt("cod_funcionario"), connBanco.rs.getString("nome_funcionario"), connBanco.rs.getString("telefone_funcionario"), connBanco.rs.getString("profissao"), connBanco.rs.getString("conselho"), connBanco.rs.getString("cpf_funcionario"), connBanco.rs.getString("rg_funcionario"), connBanco.rs.getString("data_de_nascimento")});
                
            }while(connBanco.rs.next());
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Buscar novo funcionário");
        }
        ModeloTabela modelo= new ModeloTabela(dados, colunas);
        
        jTableFuncionarios.setModel(modelo);
        jTableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTableFuncionarios.getColumnModel().getColumn(0).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTableFuncionarios.getColumnModel().getColumn(1).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTableFuncionarios.getColumnModel().getColumn(2).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(3).setPreferredWidth(120);
        jTableFuncionarios.getColumnModel().getColumn(3).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(4).setPreferredWidth(120);
        jTableFuncionarios.getColumnModel().getColumn(4).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(5).setPreferredWidth(120);
        jTableFuncionarios.getColumnModel().getColumn(5).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(6).setPreferredWidth(130);
        jTableFuncionarios.getColumnModel().getColumn(6).setResizable(false);        
        jTableFuncionarios.getColumnModel().getColumn(7).setPreferredWidth(130);
        jTableFuncionarios.getColumnModel().getColumn(7).setResizable(false);  
        jTableFuncionarios.getTableHeader().setReorderingAllowed(false);
        jTableFuncionarios.setAutoResizeMode(jTableFuncionarios.AUTO_RESIZE_OFF);
        jTableFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        connBanco.desconecta();
        
    }
   
    
